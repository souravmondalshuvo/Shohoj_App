final Map<String, CourseInfo> kCourseDB = {
  for (final (code, name, cr) in _kRaw)
    code: CourseInfo(code, name, cr),
};

final List<CourseInfo> kAllCourses =
    _kRaw.map((e) => CourseInfo(e.$1, e.$2, e.$3)).toList();

List<CourseInfo> searchCourses(String query) {
  if (query.isEmpty) return [];
  final q = query.toUpperCase();
  final exact = <CourseInfo>[];
  final prefix = <CourseInfo>[];
  final contains = <CourseInfo>[];
  for (final c in kAllCourses) {
    if (c.code == q) {
      exact.add(c);
    } else if (c.code.startsWith(q)) {
      prefix.add(c);
    } else if (c.code.contains(q) || c.name.toUpperCase().contains(q)) {
      contains.add(c);
    }
  }
  return [...exact, ...prefix, ...contains];
}
